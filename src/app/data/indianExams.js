function createObjectiveExam({
	id,
	name,
	stream,
	group,
	description,
	fullLengthQuestions,
	durationMinutes,
	defaultDifficulty = 'intermediate',
	defaultNumQuestions = 20,
	syllabus = [],
}) {
	return {
		id,
		name,
		stream,
		group,
		description,
		questionFormat: 'objective',
		availableLanguages: BILINGUAL_LANGUAGES,
		fullLengthQuestions,
		durationMinutes,
		defaultDifficulty,
		defaultNumQuestions,
		syllabus,
	};
}

